package basics;

public class forloop {
    public static void main(String[] args) {
        //int i;

        for(int i = 1; i <= 5; i++)
        {
            System.out.printf("%d iteration \n", + i);
        }

        int i = 5;
        while (i > 0)
            System.out.println("Hello Java " + i);
            i--;
    }
}